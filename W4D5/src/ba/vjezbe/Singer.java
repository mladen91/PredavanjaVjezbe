package ba.vjezbe;

public class Singer {

	public String name;
	public boolean isBand;
	public Integer activationYear;
	public String genre;
	
	public Singer(String name, boolean isBand, Integer activationYear,
			String genre) {
		
		this.name = name;
		this.isBand = isBand;
		this.activationYear = activationYear;
		this.genre = genre;
	}
	
	
	
}
